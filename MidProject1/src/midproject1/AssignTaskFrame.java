    private void cmbProjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProjectActionPerformed
        // TODO add your handling code here:
        // Clear previous combo box selections
        cmbTask.removeAllItems();
        cmbUser.removeAllItems();

        // Get the selected project name
        String selectedProjectName = (String) cmbProject.getSelectedItem();

        // Find the selected project
        Project selectedProject = null;
        for (Project p : ProjectManager.projectList) {
            if (p.getProjectName().equals(selectedProjectName)) {
                selectedProject = p;
                break;
            }
        }

        // If the project was found, add its tasks to the combo box
        if (selectedProject != null) {
            // Add tasks to combo box
            for (Task t : selectedProject.getTasks()) {
                cmbTask.addItem(t.getTitle());
            }

            // Find the team assigned to the project from the assignment list
            String teamName = null;

            for (Assignment a : ProjectManager.getAssignments()) {
                if (a.getProjectName().equals(selectedProjectName)) {
                    teamName = a.getTeamName();
                    break;
                }
            }

            if (teamName != null) {
                Team team = ProjectManager.findTeamByName(teamName);
                if (team != null) {
                    for (User u : team.getMembers()) {
                        cmbUser.addItem(u.getUsername());
                    }
                }
            }

        }
    }//GEN-LAST:event_cmbProjectActionPerformed

