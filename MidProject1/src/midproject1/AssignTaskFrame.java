    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        // Get selected values from combo boxes
        String selectedProjectName = (String) cmbProject.getSelectedItem();
        String selectedTaskTitle = (String) cmbTask.getSelectedItem();
        String selectedUsername = (String) cmbUser.getSelectedItem();

        // Null check
        if (selectedProjectName == null || selectedTaskTitle == null || selectedUsername == null) {
            JOptionPane.showMessageDialog(this, "Please make all selections.");
            return;
        }

        // Find the selected project
        Project selectedProject = null;
        for (Project p : ProjectManager.projectList) {
            if (p.getProjectName().equals(selectedProjectName)) {
                selectedProject = p;
                break;
            }
        }

        // If project not found
        if (selectedProject == null) {
            JOptionPane.showMessageDialog(this, "Project not found.");
            return;
        }

        // Find the selected task
        Task selectedTask = null;
        for (Task t : selectedProject.getTasks()) {
            if (t.getTitle().equals(selectedTaskTitle)) {
                selectedTask = t;
                break;
            }
        }

        // Find the assigned team
        String teamName = null;
        for (Assignment a : ProjectManager.getAssignments()) {
            if (a.getProjectName().equals(selectedProjectName)) {
                teamName = a.getTeamName();
                break;
            }
        }

        Team assignedTeam = ProjectManager.findTeamByName(teamName);

        if (assignedTeam == null) {
            JOptionPane.showMessageDialog(this, "Team not found.");
            return;
        }

        // Find the selected user
        User selectedUser = null;
        for (User u : assignedTeam.getMembers()) {
            if (u.getUsername().equals(selectedUsername)) {
                selectedUser = u;
                break;
            }
        }

        if (selectedUser == null || selectedTask == null) {
            JOptionPane.showMessageDialog(this, "Task or user not found.");
            return;
        }

        // Rule: Check if the task is already assigned
        if (selectedTask.getAssignedUser() != null) {
            JOptionPane.showMessageDialog(this, "This task is already assigned.");
            return;
        }

        // Rule: Check if the user already has a task in the project
        for (Task t : selectedProject.getTasks()) {
            if (selectedUser.equals(t.getAssignedUser())) {
                JOptionPane.showMessageDialog(this, "This user already has a task assigned in this project.");
                return;
            }
        }

        // Assign the task to the user
        selectedTask.setAssignedUser(selectedUser);
        JOptionPane.showMessageDialog(this, "Task successfully assigned!");
    }//GEN-LAST:event_btnAssignActionPerformed

