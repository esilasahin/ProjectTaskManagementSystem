    // Load all users from the userList and display them in the table
    private void loadUsers() {
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        model.setRowCount(0); // Clear the table before loading new data

        for (User user : userList) {
            Object[] row = {user.getFirstName(), user.getLastName(), user.getUsername(),
                user.getTeamName() != null ? user.getTeamName() : "No team"};
            model.addRow(row); // Add each user as a new row in the table
        }
    }

    // Load all teams into the combo box for selection
    private void loadTeams() {
        teamComboBox.removeAllItems(); // Clear the combo box before adding new items

        for (Team team : Team.getAllTeams()) {
            teamComboBox.addItem(team.getTeamName()); // Add team names to the combo box
        }
    }

