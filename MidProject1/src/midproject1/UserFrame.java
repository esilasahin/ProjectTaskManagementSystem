    // Event handler for 'Assign to team' button - assigns selected user to a selected team
    private void assignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignButtonActionPerformed
        int selectedRow = userTable.getSelectedRow();
        String selectedTeamName = (String) teamComboBox.getSelectedItem();

        // Ensure a user and team are selected
        if (selectedRow == -1 || selectedTeamName == null) {
            JOptionPane.showMessageDialog(null, "Please select both a user and a team.");
            return;
        }

        String username = (String) userTable.getValueAt(selectedRow, 2);
        // Find the selected user from the user list
        User selectedUser = UserManager.getUserByUsername(username);
        Team selectedTeam = null;

        // Find the selected team from the list of all teams
        for (Team team : Team.getAllTeams()) {
            if (team.getTeamName().equals(selectedTeamName)) {
                selectedTeam = team;
                break;
            }
        }

        // Assign the user to the selected team if they are not already assigned
        if (selectedUser != null && selectedTeam != null) {
            if (selectedUser.getTeamName() != null) {
                JOptionPane.showMessageDialog(this, "This user is already assigned to a team.");
                return;
            }

            // Perform the assignment
            selectedUser.setTeam(selectedTeam);
            selectedTeam.addMember(selectedUser);
            JOptionPane.showMessageDialog(this, "User " + selectedUser.getUsername() + " assigned to team " + selectedTeam.getTeamName());
            loadUsers(); // Update the user table after assignment
        }
    }//GEN-LAST:event_assignButtonActionPerformed

